private void initDbAndSeedAdmin() {
    try {
            MongoDatabase db = MongoConnection.getDatabase();
            usuariosCol = db.getCollection("usuarios");
            rolesCol = db.getCollection("roles");

            // ====== ROLES POR DEFECTO ======

            if (rolesCol.find(Filters.eq("nombre", "admin")).first() == null) {
                Document rolAdmin = new Document("nombre", "admin")
                        .append("descripcion", "Administrador general del sistema")
                        .append("permisos", Arrays.asList(
                                "CATALOGO_CLIENTE",
                                "BODEGA",
                                "GESTION_PRODUCTOS",
                                "GESTION_PEDIDOS",
                                "GESTION_USUARIOS",
                                "GESTION_ROLES"
                        ));
                rolesCol.insertOne(rolAdmin);
            }

            if (rolesCol.find(Filters.eq("nombre", "cliente")).first() == null) {
                Document rolCliente = new Document("nombre", "cliente")
                        .append("descripcion", "Cliente que realiza compras en el cat√°logo")
                        .append("permisos", Arrays.asList(
                                "CATALOGO_CLIENTE"
                        ));
                rolesCol.insertOne(rolCliente);
            }

            if (rolesCol.find(Filters.eq("nombre", "usuario")).first() == null) {
                Document rolUsuario = new Document("nombre", "usuario")
                        .append("descripcion", "Rol por defecto para clientes registrados")
                        .append("permisos", Arrays.asList(
                                "CATALOGO_CLIENTE"
                        ));
                rolesCol.insertOne(rolUsuario);
            }

            if (rolesCol.find(Filters.eq("nombre", "bodeguero")).first() == null) {
                Document rolBodeguero = new Document("nombre", "bodeguero")
                        .append("descripcion", "Responsable del stock f√≠sico en bodega")
                        .append("permisos", Arrays.asList(
                                "CATALOGO_CLIENTE",
                                "BODEGA"
                        ));
                rolesCol.insertOne(rolBodeguero);
            }

            if (rolesCol.find(Filters.eq("nombre", "gestorProductos")).first() == null) {
                Document rolGestor = new Document("nombre", "gestorProductos")
                        .append("descripcion", "Gestiona cat√°logo y pedidos")
                        .append("permisos", Arrays.asList(
                                "CATALOGO_CLIENTE",
                                "GESTION_PRODUCTOS",
                                "GESTION_PEDIDOS"
                        ));
                rolesCol.insertOne(rolGestor);
            }

            // ====== ADMIN POR DEFECTO ======
            Document existe = usuariosCol.find(Filters.eq("usuario", "estebanmoises")).first();
            if (existe == null) {

                // Password hasheado
                String passwordSeguro = PasswordUtils.generarPasswordSeguro("estebanmoises");

                Document admin = new Document("nombre", "Esteban")
                        .append("apellido", "Moises")
                        .append("cedula", "0000000000")
                        .append("usuario", "estebanmoises")
                        .append("password", passwordSeguro)
                        .append("rol", "admin")
                        .append("roles", Arrays.asList("admin"));

                usuariosCol.insertOne(admin);
                System.out.println("‚úÖ Admin 'estebanmoises' creado en MongoDB (con hash).");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "No pude conectar a MongoDB: " + e.getMessage());
        }
}
private void doLogin() {
      if (usuariosCol == null) {
            JOptionPane.showMessageDialog(this, "Base de datos no disponible");
            return;
        }

        String user = jTextUser.getText().trim();
        String pass = new String(jTextPass.getPassword());

        if (user.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa usuario y contrase√±a");
            return;
        }

        Document u = usuariosCol.find(Filters.eq("usuario", user)).first();

        boolean ok = false;
        if (u != null) {
            String saved = u.getString("password");

            // Verifica hash o texto plano (compatibilidad)
            ok = PasswordUtils.verificarPassword(pass, saved);

            // Si inici√≥ con texto plano, migramos a hash autom√°ticamente
            if (ok && saved != null && !PasswordUtils.esHash(saved)) {
                String nuevoHash = PasswordUtils.generarPasswordSeguro(pass);
                usuariosCol.updateOne(Filters.eq("_id", u.getObjectId("_id")),
                        Updates.set("password", nuevoHash));
                System.out.println("üîê Password migrado a hash para usuario: " + user);
            }
        }

        if (!ok) {
            JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos");
            return;
        }

        // ==== ROL ====
        String rol = u.getString("rol");
        if (rol == null || rol.isEmpty()) {
            rol = "cliente";
        }

        // ==== PERMISOS ====
        List<String> permisos = java.util.Collections.emptyList();
        if (rolesCol != null) {
            Document rolDoc = rolesCol.find(Filters.eq("nombre", rol)).first();
            if (rolDoc != null && rolDoc.get("permisos") != null) {
                permisos = (List<String>) rolDoc.get("permisos");
            }
        }

        this.dispose();
        new MenuPrincipalFrame(user, rol, permisos).setVisible(true);
    }
