// Regex
    private static final String REGEX_NOMBRE = "^[A-Za-z츼칄칈칍칔칖칌치칠칤칩칰칲침\\s]+$";
    private static final String REGEX_CEDULA = "^\\d{10}$";
    private static final String REGEX_PASSWORD = "^[A-Za-z0-9]+$";

    private void initDb() {
        try {
            MongoDatabase db = MongoConnection.getDatabase();
            usuariosCol = db.getCollection("usuarios");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No pude conectar a MongoDB: " + ex.getMessage());
        }
    }

    private void onSignUp() {
        if (usuariosCol == null) {
            JOptionPane.showMessageDialog(this, "BD no disponible");
            return;
        }

        String nombre = jTextFieldNombre.getText().trim();
        String apellido = jTextFieldApellido.getText().trim();
        String cedula = jTextFieldCedula.getText().trim();
        String usuario = jTextFieldUsuario.getText().trim();
        String pass1 = jTextFieldContrase침a.getText().trim();
        String pass2 = jTextFieldContrase침a2.getText().trim();

        // ---- Validaciones ----
        if (nombre.isEmpty() || apellido.isEmpty() || cedula.isEmpty()
                || usuario.isEmpty() || pass1.isEmpty() || pass2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }

        if (!nombre.matches(REGEX_NOMBRE)) {
            JOptionPane.showMessageDialog(this, "El nombre solo debe contener letras y espacios.");
            return;
        }

        if (!apellido.matches(REGEX_NOMBRE)) {
            JOptionPane.showMessageDialog(this, "El apellido solo debe contener letras y espacios.");
            return;
        }

        if (!cedula.matches(REGEX_CEDULA)) {
            JOptionPane.showMessageDialog(this, "La c칠dula debe tener exactamente 10 n칰meros.");
            return;
        }

        if (!pass1.matches(REGEX_PASSWORD)) {
            JOptionPane.showMessageDialog(this, "La contrase침a solo puede contener letras y n칰meros.");
            return;
        }

        if (!pass1.equals(pass2)) {
            JOptionPane.showMessageDialog(this, "Las contrase침as no coinciden.");
            return;
        }

        // ---- Unicidad ----
        if (usuariosCol.find(com.mongodb.client.model.Filters.eq("usuario", usuario)).first() != null) {
            JOptionPane.showMessageDialog(this, "El usuario ya existe.");
            return;
        }

        if (usuariosCol.find(com.mongodb.client.model.Filters.eq("cedula", cedula)).first() != null) {
            JOptionPane.showMessageDialog(this, "La c칠dula ya est치 registrada.");
            return;
        }

        // ---- Roles ----
        // Tu sistema ya maneja roles desde ADMIN
        // Por defecto el usuario se registra como cliente.
        List<String> rolesBase = Arrays.asList("cliente");

        // ---- Hash de password ----
        String passwordSeguro = PasswordUtils.generarPasswordSeguro(pass1);

        Document nuevo = new Document("nombre", nombre)
                .append("apellido", apellido)
                .append("cedula", cedula)
                .append("usuario", usuario)
                .append("password", passwordSeguro) // 游댏 ahora hash
                .append("rol", "cliente")
                .append("roles", rolesBase);

        try {
            usuariosCol.insertOne(nuevo);
            JOptionPane.showMessageDialog(this, "Usuario creado 九\nAhora puede iniciar sesi칩n.");
            dispose();
            new Login().setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al registrar: " + ex.getMessage());
        }
    }

